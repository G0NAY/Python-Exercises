# Colas

cola = ["Maria", "Pedro", "Alejandro", "Pepe"]
print(cola)

# Agregamos elementis al final de la cola
cola.append("Karla")
print(cola)

# Sacando elementos por el principio de la cola, donde 0 inidca la posicion del primer elemento
n = cola.pop(0)
print(f"Atendiendo a {n}")
print(cola)

